# 연습문제2. 파일크기 계산하기
"""
파일을 다운로드할 때의 평균 속도를 r이라 하고, 
다운로드하는 데 걸린 시간을 t라고 할 때, 
다운로드한 파일의 용량은 r x t로 계산할 수 있습니다.
"""
r = 800
t = 110
KB = r * t / 1000
print(KB,"KB입니다.")

